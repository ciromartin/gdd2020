	  FECHA_ID
	 ,CASE 
		WHEN (FLOOR(DATEDIFF(DAY, CLIENTE_FECHA_NAC, GETDATE()) / 365.25)) BETWEEN 18 AND 30 THEN 1
		WHEN (FLOOR(DATEDIFF(DAY, CLIENTE_FECHA_NAC, GETDATE()) / 365.25)) BETWEEN 31 AND 50 THEN 2
