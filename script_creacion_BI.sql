		ON C.COMPRA_NRO = CA.COMPRA_NRO
	INNER JOIN [WHITE_WALKERS].[CLIENTE] CL 
		ON C.ID_CLIENTE=CL.ID_CLIENTE
	INNER JOIN [WHITE_WALKERS].[AUTO] A
		ON CA.ID_AUTO = A.ID_AUTO
