SELECT 
	ANIO AS AÑO,
	MES_CODIGO AS MES,
	MODELO_CODIGO AS MODELO,
	0 AS CANTIDAD_AUTOS_COMPRADOS,
	SUM(CANTIDAD_AUTOS_VENDIDOS) AS CANTIDAD_AUTOS_VENDIDOS
