	ANIO,
	MES_DESC,
	T3.ID_SUCURSAL

UNION

SELECT 
	ANIO AS AÑO,
	MES_DESC AS MES,
	T3.ID_SUCURSAL AS SUCURSAL,
	0 AS COMPRA,
	SUM(PRECIO_TOTAL_VENTA) AS VENTA
