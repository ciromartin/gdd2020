	[WHITE_WALKERS].[BI_HECHO_VENTAS_AUTOS] T1 
	INNER JOIN [WHITE_WALKERS].[BI_DIM_TIEMPO] T2 ON T1.FECHA_ID=T2.FECHA_ID
